    // ÊâπÈáèÊà™Âõæ
    async captureMultiple(selectors, options = {}) {
        const results = [];
        
        for (const selector of selectors) {
            try {
                const capture = await this.captureElement(selector, options);
                results.push({
                    selector,
                    success: true,
                    data: capture
                });
            } catch (error) {
                results.push({
                    selector,
                    success: false,
                    error: error.message
                });
            }
        }
        
        return results;
    }

    // ÂàõÂª∫Êà™ÂõæÈ¢ÑËßà
    createPreview(imageData, maxWidth = 300, maxHeight = 200) {
        return new Promise((resolve, reject) => {
            const img = new Image();
            
            img.onload = () => {
                const canvas = document.createElement('canvas');
                const ctx = canvas.getContext('2d');
                
                // ËÆ°ÁÆóÁº©ÊîæÊØî‰æã
                const scale = Math.min(maxWidth / img.width, maxHeight / img.height);
                canvas.width = img.width * scale;
                canvas.height = img.height * scale;
                
                // ÁªòÂà∂Áº©ÊîæÂêéÁöÑÂõæÂÉè
                ctx.drawImage(img, 0, 0, canvas.width, canvas.height);
                
                resolve(canvas.toDataURL());
            };
            
            img.onerror = reject;
            img.src = imageData;
        });
    }

    // ‰øùÂ≠òÊà™ÂõæÂà∞Êñá‰ª∂
    async saveToFile(imageData, filename) {
        try {
            // Â∞Übase64Êï∞ÊçÆËΩ¨Êç¢‰∏∫buffer
            const base64Data = imageData.replace(/^data:image\/\w+;base64,/, '');
            const buffer = Buffer.from(base64Data, 'base64');
            
            // ‰ΩøÁî®ElectronÁöÑ‰øùÂ≠òÂØπËØùÊ°Ü
            const result = await window.electronAPI.saveFile({
                type: 'image',
                data: buffer,
                filename: filename || `screenshot_${Date.now()}.png`
            });
            
            return result;
        } catch (error) {
            console.error('Save screenshot failed:', error);
            throw new Error(`‰øùÂ≠òÊà™ÂõæÂ§±Ë¥•: ${error.message}`);
        }
    }

